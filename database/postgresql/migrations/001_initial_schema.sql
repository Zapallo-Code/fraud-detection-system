CREATE TABLE IF NOT EXISTS public.alert_log (
    -- Unique internal identifier for the alert event.
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    -- Alert category/type (for example, HIGH_FRAUD_RATE or DRIFT_DETECTED).
    alert_type TEXT NOT NULL,

    -- Operational severity level of the alert.
    severity TEXT NOT NULL,

    -- Human-readable alert message for operators.
    message TEXT NOT NULL,

    -- Timestamp when the alert was triggered.
    triggered_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    -- Timestamp when the alert was acknowledged.
    acknowledged_at TIMESTAMPTZ,

    -- User or operator that acknowledged the alert.
    acknowledged_by TEXT,

    CONSTRAINT alert_log_severity_ck
        CHECK (severity IN ('INFO', 'WARNING', 'HIGH', 'CRITICAL'))
);

CREATE INDEX IF NOT EXISTS idx_alert_log_triggered_at
    ON public.alert_log (triggered_at DESC);

CREATE INDEX IF NOT EXISTS idx_alert_log_open_unack
    ON public.alert_log (triggered_at DESC)
    WHERE acknowledged_at IS NULL;

CREATE INDEX IF NOT EXISTS idx_alert_log_severity
    ON public.alert_log (severity);

-- ============================================================================
-- SECTION 5: audit_log table
-- ============================================================================

CREATE TABLE IF NOT EXISTS public.audit_log (
    -- Unique internal identifier for the audit record.
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    -- Name of the table affected by the audited operation.
    "table" TEXT NOT NULL,

    -- Operation performed on the affected record.
    "operation" TEXT NOT NULL,

    -- Database user that performed the operation.
    "user" TEXT NOT NULL DEFAULT CURRENT_USER,

    -- Timestamp when the audit event was captured.
    "timestamp" TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    -- Row state before the operation (NULL for INSERT).
    old_values JSONB,

    -- Row state after the operation (NULL for DELETE).
    new_values JSONB,

    CONSTRAINT audit_log_operation_ck
        CHECK ("operation" IN ('INSERT', 'UPDATE', 'DELETE', 'ACTIVATE'))
);

CREATE INDEX IF NOT EXISTS idx_audit_log_table
    ON public.audit_log ("table");

CREATE INDEX IF NOT EXISTS idx_audit_log_timestamp
    ON public.audit_log ("timestamp" DESC);

CREATE INDEX IF NOT EXISTS idx_audit_log_user
    ON public.audit_log ("user");

